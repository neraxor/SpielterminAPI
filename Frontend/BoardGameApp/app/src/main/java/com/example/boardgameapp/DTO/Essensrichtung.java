package com.example.boardgameapp.DTO;

public class Essensrichtung {
    private int id;
    private String art;
    public Essensrichtung() {}

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getArt() {
        return art;
    }

    public void setArt(String art) {
        this.art = art;
    }
}
